using System;
using System.Collections.Generic;
using System.Text;

namespace BasicMath
{
    public class Trigonometric
    {
        public static double Tan(double a)
        {
            return (Math.Tan(a));
        }

        public static double Sin(double a)
        {
            return (Math.Sin(a));
        }

        public static double Cos(double a)
        {
            return (Math.Cos(a));
        }
    }
}
